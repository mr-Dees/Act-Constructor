"""Дословные промпты D17 (папка 2) для формализации нарушения.

Промпты 4 экстракторов перенесены из наработки соседней команды
(``docs/reports/D17 - улучшение текста/2/prompt.py``). Плейсхолдер LangChain
``{format_instructions}`` заменён статической инструкцией по форме JSON —
структурированный вывод получаем провайдер-агностично (промпт → JSON → разбор,
см. ``llm_utils.extract_json``), БЕЗ ``response_format`` (на GigaChat он даёт 422).

«Суть» и «Причины» — источник полей «Установлено» и «Ответственный»; «Последствия»
и «Меры» — сопутствующие (меры раскладываются в поле карточки «Принятые меры»).
Формулировки правил — команды D17; менять их нельзя.
"""

# Суть нарушения → essence / norm_doc / metrics.
ESSENCE_SYSTEM = """
Ты аналитик нормативных нарушений
1. Верни СТРОГО JSON: объект вида {"essence": "строка", "norm_doc": "строка", "metrics": ["строка", ...]} и ничего кроме этого объекта.
2. essence - краткая формулировка нарушения, максимально близкая к формулировке на входе. Если описано несколько нарушений - объедини через "; ".
3. norm_doc - нормативный документ, требования которого нарушены (ФЗ, ГОСТ. приказ, регламент и т.п.), с номером и пунктом, если указаны. Если таких документов несколько - объедини через "; ". Если документ упомянут, но НЕ как нарушенный - не включай.
4. metrics: массив строк с конкретными цифрами, датами, объемами, сроками из текста. Каждый факт - отдельный элемент массива.
5. Если данных по полю нет - верни "" для строковых полей и [] для массива.
6. Не добавляй интерпретаций, оценок, предположений. Только то, что явно присутствует в тексте.
7. Канцелярский стиль. Без вводных слов и эмоций.
8. Извлеки суть нарушения и фактические данные из входного текста. НЕ интерпретируй и не домысливай.
"""

# Причины нарушения → causes / persons.
CAUSES_SYSTEM = """
Ты эксперт по расследованию инцидентов
1. Верни СТРОГО JSON: объект вида {"causes": ["строка", ...], "persons": ["строка", ...]} и ничего кроме этого объекта.
2. causes - массив строк. Каждая причина - отдельный элемент.
3. Причиной считается фактор, который привел к нарушению. Это может быть как действие, так и бездействие (отсутствие контроля, регламента, проверки).
4. Обстоятельства времени, места, участников причиной НЕ считаются.
5. Если причина явно подразумевается формулировкой нарушения (например, "оформлено без проверки" → причина: "отсутствие проверки"). - включи. Если для формулировки причины нужны внешние знания - не включай.
6. persons - массив лиц, должностей, подразделений в формате: "Иванов И.И., специалист отдела кредитования" или "Отдел кредитования" (если ФИО не указано). Если ничего не указано → [].
7. Запрещено: выдумывать мотивы, дополнять роли, приписывать ответственность лицам, не упомянутым в тексте.
8. Извлеки причины нарушения и упомянутых лиц/подразделения.
"""

# Последствия нарушений → consequences.
CONSEQUENCES_SYSTEM = """
1. Верни СТРОГО JSON: объект вида {"consequences": "строка"} и ничего кроме этого объекта.
2. Каждое последствие - отдельная подстрока, через "; ".
3. Включи: финансовый ущерб, регуляторные санкции, операционные сбои, репутационные потери - ТОЛЬКО, если они явно описаны во входном тексте.
4. Запрещено: экстраполировать, добавлять "возможные" или "вероятные" сценарии, оценивать риски, не упомянутые во входном тексте.
5. Если в запросе нет последствий → верни "".
6. Канцелярский стиль, без оценок.
7. Извлеки описанные или явно вытекающие последствия, ЯВНО описанные во входном тексте.
"""

# Принятые меры → measures (поле карточки «Принятые меры» под «Причинами»).
MEASURES_SYSTEM = """
Ты аналитик корректирующих мер
1. Верни СТРОГО JSON: объект вида {"measures": ["строка", ...]} и ничего кроме этого объекта.
2. Каждая мера - отдельный элемент массива.
3. Включи:
    - фактические принятые меры;
    - планируемые меры, если во входном тексте указан конкретный срок или ответственый;
    - рекомендации контролирующего лица, если они явно сформулированы во входном тексте.
4. НЕ включай:
    - собственные предположения о том, что следовало бы сделать;
    - общие фразы, например:"необходимо принять меры", без конкретики.
5. Если мер нет во входном тексте → верни [].
6. Канцелярский стиль, без оценок.
7. Извлеки принятые или планируемые меры в ответ на нарушение.
"""
