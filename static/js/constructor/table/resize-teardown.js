/**
 * Идемпотентная обёртка для разборки слушателей ресайза колонок.
 *
 * Ресайз колонки вешает временные слушатели (mousemove/mouseup) и должен снять
 * их РОВНО ОДИН раз: либо на нормальном mouseup, либо на прерывании (потеря
 * фокуса при alt-tab, отмена указателя). Без защиты от повторного вызова
 * teardown сработал бы дважды (например, blur + последующий mouseup), что
 * повторно зафиксировало бы веса и лишний раз перерисовало таблицу.
 *
 * Чистая функция без DOM — тестируется в node:test.
 */

/**
 * Оборачивает функцию разборки так, чтобы она выполнилась не более одного раза.
 * Повторные вызовы — no-op.
 * @param {() => void} fn Функция разборки (снятие слушателей + финализация).
 * @returns {() => void} Идемпотентная обёртка.
 */
export function makeIdempotentTeardown(fn) {
    let done = false;
    return () => {
        if (done) return;
        done = true;
        fn();
    };
}
